									<div class="text">
										<p>Di tengah berbagai tantangan pertanian yang dihadapi saat ini ataupun ke depan, Indonesia perlu mengembangkan sistem pangan nasional yang antisipatif untuk menjamin ketersediaan pangan. Hal ini perlu dilakukan dengan berbagai macam strategi, mulai dari pengembangan pangan lokal, peningkatan pertanian di lahan kering, hingga memperhatikan aspek penyuluh lapangan.

                                            Guru Besar Fakultas Pertanian IPB University Hadi Susilo Arifin menyampaikan, pertanian harus bisa menghadapi sejumlah tantangan saat ini ataupun masa depan, seperti peningkatan populasi manusia, perubahan pola hidup, dan kebutuhan akan sistem produksi ramah lingkungan.
                                            
                                            ”Adanya perdagangan bebas pada akhirnya membuat konsumen menentukan aturan dan regulasi kepada negara produsen. Begitu juga masyarakat kota yang sekarang tidak makan sembarangan karena sadar gaya hidup sehat,” ujarnya dalam webinar bertajuk ”Konsep Kemandirian Pangan Lokal untuk Meningkatkan Kesejahteraan Petani”, Sabtu (30/10/2021).
                                            
                                            Berkaca dari sejumlah tantangan tersebut, Hadi menyatakan perlu sistem pangan nasional yang antisipatif untuk menjamin ketersediaan pangan yang cukup. Hal ini diperlukan mengingat Indonesia menjadi salah satu negara yang rentan terhadap kekurangan pangan. Indeks ketahanan pangan global Indonesia tercatat berada pada peringkat ke-62 dari 113 negara.</p>
										<p>Menurut Hadi, menciptakan sistem pangan nasional yang antisipatif dapat dilakukan dengan mengembangkan keanekaragaman pangan lokal tidak hanya padi, tetapi juga sagu, singkong, talas, hingga ubi jalar. Pengembangan ragam pangan lokal ini harus inovatif mulai dari pascapanen, logistik, pemrosesan, pengiriman, hingga melakukan kampanye.</p>
										<p>Untuk mencapai ketahanan pangan, Indonesia juga perlu mengembangkan pertanian di lahan kering. Sebab, Indonesia memiliki lahan basah yang terbatas. Data menunjukkan, dari total 191,1 juta hektar daratan Indonesia, seluas 43,6 juta hektar merupakan lahan basah dan 144,5 juta hektar lahan kering.

                                            ”Sawah untuk komoditas padi dibangun di lahan basah. Jadi, jika kita terus mengonsumsi beras, akan membuka sawah baru dan ini tidak mudah. Kita memiliki lahan kering yang luas dan pangan lokal lebih banyak memanfaatkan lahan ini,” tuturnya.
                                            
                                            Baca juga : Tingkatkan Kesadaran Publik terhadap Pangan Lokal
                                            
                                            Strategi lain dalam mengembangkan pangan lokal, menurut Hadi, dengan meningkatkan dukungan logistik dari kebun hingga meja makan. Dukungan logistik ini salah satunya menyangkut penyediaan tempat penyimpanan yang layak untuk semua komoditas.
                                            
                                            Selain itu, pengembangan pangan lokal juga harus meningkatkan layanan ekstensi berupa penyuluh pertanian. Di beberapa daerah, penyuluh pertanian kurang mendapat perhatian karena keterbatasan jumlah sumber daya manusia dan pendapatan yang rendah. Padahal, kata Hadi, penyuluh pertanian merupakan aspek yang sangat penting agar petani bisa terus mengembangkan keterampilannya termasuk menggunakan internet.</p>
                                        <h5> Potensi pangan lokal </h5>
										<p>Kepala Balai Pengkajian Teknologi Pertanian (BPTP) Maluku Procula Rudlof mengatakan, banyak potensi sumber daya pangan Indonesia dari daerah-daerah. Tercatat Indonesia memiliki 77 jenis sumber karbohidrat, 75 jenis sumber protein, 26 jenis kacang-kacangan, 228 jenis sayuran, serta 110 jenis rempah dan bumbu. Bahan pangan berpati seperti ubi kayu, jagung, dan sagu juga dapat diolah menjadi tepung sebagai substitusi terigu.</p>
										<p>Rudlof menilai bahwa pangan lokal memiliki peran dalam memperkuat daya tahan produksi pangan nasional. Pangan lokal secara alamiah sudah beradaptasi dengan karakteristik sumber daya alam dan sosial budaya setempat.

                                            Di samping itu, produksi pangan lokal kerap dilakukan dengan menggunakan faktor-faktor produksi yang tersedia secara lokal sehingga mengurangi risiko terhambatnya pasokan produksi dari luar. Di sisi lain, pangan lokal juga telah dikonsumsi penduduk setempat yang terbatas sehingga mengurangi biaya dan risiko pemasaran.
                                            
                                            Baca juga : Mengapa Pangan Lokal Masa Depan Kita
                                            
                                            Rudlof menegaskan, upaya pengembangan pangan lokal sangat penting karena aspek geografi Indonesia sebagai negara kepulauan, pulau-pulau kecil, dan perbatasan. Pola makan pangan lokal juga sangat identik dengan pelestarian budaya, kearifan lokal, dan norma-norma sosial yang dibangun masyarakat setempat.
                                            
                                            ”Pengembangan pangan lokal juga sangat penting sebagai strategi kelangkaan pangan dunia akibat permasalahan produksi. Pangan lokal kita seperti porang bahkan terus berkembang ke industrialisasi dan menjadi kepentingan negara-negara yang membutuhkan,” ucapnya.
                                            
                                            </p>
										<p>Meski demikian, Rudlof mengakui bahwa pengembangan pangan lokal masih menemui sejumlah kendala dari aspek permintaan, preferensi, dan persaingan. Permintaan dan preferensi pangan lokal saat ini masih terbatas serta harus bersaing dengan beras atau produk impor lainnya.

                                            Terkait hal ini, Rudlof menyatakan, pemerintah telah menetapkan peta jalan penurunan konsumsi beras hingga 2024. Pada saat yang sama, konsumsi ubi kayu, jagung, sagu, kentang, pisang, dan talas akan ditingkatkan hingga 1,2 juta ton tahun 2024 di setiap provinsi. Adapun strategi pengembangan yang dilakukan mulai dari promosi melalui peraturan daerah hingga penguatan usaha mikro, kecil, dan menengah (UMKM).</p>

