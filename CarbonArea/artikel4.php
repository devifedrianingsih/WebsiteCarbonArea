									<h1 ><a href="https://www.kompas.id/baca/ilmu-pengetahuan-teknologi/2021/10/30/pangan-lokal-berperan-penting-dalam-mencapai-ketahanan-pangan">Pangan Lokal Berperan Penting dalam Mencapai <br>Ketahanan Pangan</a></h1>
									<!-- Meta -->
									<div class="text>

											<span class="author" src="img/logoupaya.svg" alt="#">PRADIPTA PANDU</span>
											<span class="date"><i class="fa fa-clock-o"></i>30 Oktober 2021 19:14 WIB</span>
