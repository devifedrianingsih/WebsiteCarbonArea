										<p>Pangan dan pertanian merupakan sektor yang tidak luput terkena dampak pandemi Covid-19. Pada triwulan II 2020, sektor pertanian menyumbang 15,46 persen terhadap Produk Domestik Bruto (PDB) Indonesia. Nilai Tukar Petani (NTP) nasional juga tercatat masih cukup rendah. Selain isu tersebut, sampah makanan juga menjadi salah satu dampak pandemi. setiap tahunnya, satu orang menghasilkan 300 kilogram sampah makanan setara 13 juta ton. “Penting bagi kita untuk melakukan aksi untuk mengurangi akibat negatif dari pandemi Covid-19,” ujar Kepala Subdirektorat Peternakan Direktorat Pangan dan Pertanian Kementerian PPN/Bappenas Noor Avianto pada Webinar Reducing Food Loss and Waste During Covid-19, Selasa (29/9).

                                            Untuk mengatasi krisis pangan dan pertanian yang juga terjadi akibat perubahan iklim dan gejolak ekonomi global sebelum pandemi Covid-19, Kementerian PPN/Bappenas telah menyiapkan beberapa strategi. Pertama, meningkatkan penambahan nutrisi pangan. Kedua, meningkatkan ketersediaan bahan pangan dengan harga yang stabil dan keberlanjutan. Kedua strategi tersebut didukung dengan strategi ketiga, yaitu peningkatan sumber daya manusia untuk meningkatkan produktivitas pertanian dan kemudahan akses pasar. Keempat, peningkatan sumber daya alam di sektor pertanian. Strategi kelima adalah memperbaiki sistem pangan.
                                            
                                            Perbaikan sistem pangan ini sejalan dengan peningkatan produksi pangan domestik dan industri makanan lokal. Untuk itu, diperlukan stimulus ekonomi bagi pengusaha, baik penghasil makanan maupun petani untuk dapat menghasilkan produk dengan kualitas yang lebih baik. Akses pangan yang stabil juga diperlukan untuk menjamin distribusi merata ke semua lapisan masyarakat. “Bantuan pangan terhadap rumah tangga yang rentan juga penting. Pilar ini fokus untuk memberikan pangan yang memadai bagi semua orang. Bersamaan dengan hal itu, pilar ini diharapkan juga dapat mengurangi sampah makanan yang dihasilkan,” imbuh Noor.
                                            
                                            Berdasarkan asalnya, terdapat dua jenis sampah makanan. Sampah makanan yang berasal dari hasil produksi pertanian, perikanan, maupun peternakan, dan distribusi disebut food loss. Sedangkan sampah makanan yang dihasilkan dari konsumsi individu, memasak, dan makan disebut food waste. Untuk mengurangi food loss, diperlukan praktik pertanian dan distribusi yang baik. Saat ini, kehilangan bahan pangan banyak terjadi saat distribusi pengiriman barang. “Usaha mengurangi food loss dan food waste tidak hanya dilakukan oleh pemerintah dengan insentif fiskal, tetapi memerlukan kerja sama dari berbagai pihak, seperti komunitas dan sektor privat,” tandasnya.</p>

