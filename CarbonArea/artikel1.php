									<h1 ><a href="https://sda.pu.go.id/balai/bwssumatera1/article/upaya-menjaga-ketahanan-pangan-indonesia">Upaya Menjaga Ketahanan Pangan Indonesia</a></h1>
									<!-- Meta -->
									<div class="text>

											<span class="author" src="img/logoupaya.svg" alt="#">Pembangunan Nasional</span>
											<span class="date"><i class="fa fa-clock-o"></i>15/01/2020, 04:46 PM</span>
										</div>
									</div>
									<!-- News Text -->
									<div class="text">
										<p>Global Food Security Index (GFSI) atau Indeks Ketahanan Pangan Global yang dirilis The Economist dan Corteva—perusahaan sains bidang pangan—menempatkan Indonesia pada posisi 65 dari 113 negara. Di kawasan ASEAN, Indonesia bercokol di posisi kelima (per Oktober 2018).</p>
										<p>Capaian itu terbilang menggembirakan, sebetulnya, mengingat skor ketahanan pangan Indonesia—berdasar laporan yang sama—terus menunjukkan peningkatan dari tahun ke tahun. Di rentang 1-100, skor Indonesia merangkak dari 46,8 (2012) menjadi 54,8 (2018).</p>
										<p>Laporan GFSI dibuat berdasarkan empat aspek penilaian, yakni keterjangkauan, ketersediaan, kualitas dan keamanan, serta sumber daya. Untuk variabel pertama, skor yang didapat Indonesia adalah 55,2 (peringkat 63). Aspek ketersediaan meraih skor paling tinggi: 58,2; menempatkan Indonesia di posisi ke-58. Adapun perihal kualitas dan keamanan diberi skor 44,5 (peringkat 84), dan faktor sumber daya alam justru mendapatkan nilai 43,9 (peringkat 111).</p>
										<p>Lepas dari catatan di atas, Menteri Pekerjaan Umum dan Perumahan Rakyat (PUPR) Basuki Hadimuljono menyatakan pihaknya akan terus berbenah demi menjawab tantangan kebutuhan pangan di masa depan.</p>
										<p>“Produksi pangan harus dua kali lipat pada 2050 untuk memenuhi permintaan populasi dunia yang terus bertambah, sementara daratan dan ketersediaan air menjadi terbatas," kata Basuki Hadimuljono, saat memberikan sambutan di 3rd World Irrigation Forum di Bali Nusa Dua Convention Center (BNDCC), Nusa Dua, Bali, Senin (2/9/2019).</p>
										<p>Menyimak pernyataan Basuki, adalah masuk akal bila sepanjang 2015-2019, sebagaimana yang tertuang dalam Rencana Strategis (Renstra) Kementerian PUPR, pemerintah menargetkan pembangunan 65 bendungan, 1.088 embung, 1 juta hektar pembangunan jaringan irigasi baru, 3 juta hektar rehabilitasi jaringan irigasi, 306 pengendali sedimen dan lahar, penyediaan air baku 67,52 m³/detik, hingga pengendali banjir dan pengaman pantai sepanjang 3.620 km.</p>
										<p>“Ketahanan pangan harus didukung oleh ketahanan air,” kata Dirjen Sumber Daya Air (SDA) Kementerian PUPR Hari Suprayogi, Rabu (20/11). Ia juga menyampaikan, semua upaya pemerintah dilakukan demi mewujudkan visium di tahun 2030 Indonesia punya pasokan air 120 meter kubik per kapita per tahun.</p>
										<p>“Saat ini Indonesia masih 58 meter kubik per kapita per tahun. Maka untuk memenuhi target itu pembangunan tampungan harus selalu dilakukan,” kata Hari.</p>					
										<p>Hari mengakui upaya mencapai target tidaklah mudah. Pembangunan 1 bendungan saja, misalnya, bisa menghabiskan waktu 4-5 tahun. Itu pula yang menyebabkan pemerintah baru membangun 61 bendungan—45 bendungan baru masih on going dan 16 bendungan sudah selesai—dari 65 yang ditargetkan. “Itu semua tergantung readiness criteria dari desain dan lahan,” kata Hari. Ia menjelaskan, dengan proses seperti itu, 65 bendungan yang ditargetkan pemerintah baru akan rampung pada 2023.</p>
										<p>Meski begitu, capaian pemerintah tidaklah mengecewakan.
											“Pembangunan jaringan irigasi baru targetnya 1 juta hektar dan saat ini sudah 1,8 juta hektar. Kemudian rehabilitasi jaringan irigasi ekstisting sebanyak 3 juta hektar, sekarang malah sudah terpenuhi 3 juta hektar lebih,” pungkas Hari.
											Untuk diketahui, proyek pembangunan 65 bendungan membutuhkan dana 73,81 triliun rupiah. Dengan capaian saat ini, diharapkan 17 miliar meter kubik air bisa ditampung dan dimanfaatkan, salah satunya, untuk irigasi.</p>
										<p>Sebelumnya, Indonesia memiliki 231 bendungan besar yang mampu mengairi 11 persen dari luas total 7,1 juta hektar sawah. Dengan terbangunnya 65 bendungan pada 2024 mendatang, diharapkan total layanan air untuk irigasi meningkat menjadi 465 ribu hektar, atau setara 20 persen dari total sawah.
											Adapun pembangunan dan rehabilitasi jaringan irigasi yang saat ini dikerjakan pemerintah antara lain Daerah Irigasi (D.I.) Lhok Guci, Aceh (18.542 hektar), Jambo Aye Kanan, Aceh (3.028 hektar), Lematang, Sumatera Selatan (3.000 hektar), Umpu System/Wai Besai (7.500 hektar), Leuwigoong, Jawa Barat (5.313 hektar), Baliase, Sulawesi Selatan (21.925 hektar), dan Gumbasa, Sulawesi Tengah (8.103 hektar).</p>
										<p>Dalam konteks ketahanan pangan, pembangunan bendungan dan saluran irigasi itu memungkinkan petani tetap mendapat pasokan air yang cukup di musim kemarau, sehingga kebutuhan konsumsi masyarakat tetap terpenuhi, bahkan panen bisa ditingkatkan hingga 3 kali lipat dalam setahun.
											Selain pembangunan infrastruktur, upaya meningkatkan ketahanan pangan juga dilakukan dengan modernisasi irigasi yang saat ini masih terus disosialisasikan kepada para petani. “Upaya itu mencakup lima pilar: Keandalan Suplai Air; Keandalan Jaringan/Infrastruktur Irigasi; Manajemen Air; Kelembagaan Petani; Peningkatan Kualitas SDM,” kata Direktur Irigasi dan Rawa, Ditjen SDA Kementerian PUPR, Mochammad Mazid.</p>
