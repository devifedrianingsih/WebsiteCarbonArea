									<h1 ><a href="https://www.liputan6.com/bisnis/read/4231605/strategi-ketahanan-pangan-kementan-untuk-antisipasi-perubahan-iklim-2020">Strategi Ketahanan Pangan Kementan untuk Antisipasi Perubahan Iklim 2020</a></h1>
									<!-- Meta -->
									<div class="text>

											<span class="author" src="img/logoupaya.svg" alt="#">Gilar Ramdhani</span>
											<span class="date"><i class="fa fa-clock-o"></i>19 Apr 2020, 12:07 WIB</span>
