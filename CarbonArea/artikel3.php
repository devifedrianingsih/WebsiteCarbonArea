										<p>Jakarta, 14/09/2022 Kemenkeu - Ketersediaan pangan yang dapat dijangkau berbagai pihak mampu ikut menjaga stabilitas ekonomi nasional di tengah dinamika global. Maka dari itu, ketahanan pangan perlu menjadi fokus untuk ditingkatkan dengan mewujudkan pangan yang berdaulat (food sovereignty) dan mandiri (food resilience).

                                            Menteri Koordinator (Menko) Bidang Perekonomian Airlangga Hartarto dalam acara Diskusi Ekonomi Berdikari Kompas dengan tema “Ketahanan Pangan Kunci Hadapi Potensi Krisis Global”, Selasa (13/09), mengatakan Pemerintah juga melakukan upaya mendorong ketahanan pangan melalui berbagai bauran strategi dan kebijakan.
                                            
                                            “Ketahanan pangan bukan hanya menjadi prioritas tapi juga menjadi target kesejahteraan dan pemerataan kesejahteraan masyarakat, Pemerintah telah merumuskan dan mengimplementasikan kebijakan penguatan ketahanan pangan nasional,” kata Menko Airlangga secara virtual.
                                            
                                            Dari sisi pembiayaan, Pemerintah menyediakan bantuan Kredit Usaha Rakyat (KUR) yang dapat diakses oleh pelaku sektor pertanian dengan bunga yang hanya sebesar 3 persen hingga akhir tahun 2022. Selain itu, plafon kredit KUR pada tahun 2022 juga ditingkatkan hingga Rp373,17 triliun dan plafon KUR Mikro (tanpa agunan tambahan) yang sebelumnya di atas Rp10 juta hingga Rp50 juta, ditingkatkan menjadi di atas Rp10 juta hingga Rp100 juta.
                                            
                                            “Pemerintah telah meningkatkan plafon KUR menjadi sebesar Rp373,17 triliun pada tahun 2022 sehingga Pemerintah Daerah atau Kementerian bisa menggunakan baik untuk pengadaan alsintan maupun korporatisasi daripada pertanian,” ungkap Menko Airlangga.
                                            
                                            Sedangkan dari aspek kelembagaan, upaya yang dilakukan Pemerintah adalah dengan membentuk Badan Pangan Nasional melalui penerbitan Peraturan Presiden Nomor 66 Tahun 2021. Lembaga ini diberikan kewenangan terkait pengelolaan cadangan pangan Pemerintah, pelaksanaan kegiatan stabilisasi pasokan dan harga, penguatan sistem logistik pangan, pengentasan wilayah rentan rawan pangan, pengembangan penganekaragaman pangan dan pengembangan potensi pangan lokal.
                                            
                                            Untuk memperkuat Cadangan Beras Pemerintah (CBP), telah diterbitkan kebijakan pembelian gabah atau beras petani oleh Perum BULOG hingga stok CBP mencapai 1,2 juta ton setara beras. Penugasan ini ditujukan untuk memperluas kapasitas Perum BULOG dalam menyerap produksi petani pada musim gadu tahun 2022, sekaligus mencegah jatuhnya harga di tingkat petani.
                                            
                                            Selain itu, Pemerintah juga melakukan diversifikasi pangan lokal dengan meningkatkan produksi jagung, sorgum, sagu, dan singkong melalui perluasan lahan dan pembukaan area baru dalam rangka peningkatan produksi sebagai alternatif bahan pangan impor.
                                            
                                            Lebih lanjut, Menko Airlangga menjelaskan bahwa terkait ketersediaan pupuk bersubsidi, Pemerintah telah melakukan reformasi kebijakan pupuk bersubsidi dengan membatasi penyaluran pupuk bersubsidi untuk sembilan komoditas utama yakni padi, jagung, kedelai, cabai, bawang merah, bawang putih, tebu, kopi, dan kakao dengan jenis pupuk yang disalurkan adalah pupuk urea dan NPK.
                                            
                                            Pemerintah juga mengembangkan berbagai kawasan sentra mandiri pangan berbasis korporasi petani untuk meningkatkan efisiensi dan skala ekonomi produksi pertanian melalui Program Food Estate di beberapa wilayah yaitu Kalimantan Tengah, Sumatera Utara, Nusa Tenggara Timur, Jawa Tengah, Sulawesi Tengah, Papua, serta Program Closed Loop yang telah dikembangkan di Sukabumi, Garut, dan Sikka. (EKON/dep/hpy)</p>

