										<h5>Berikut ini beberapa cara mengatasi krisis pangan</h5>
                                        <p>1. Mengatasi perubahan iklim<br>
                                        Perubahan iklim berdampak parah pada mata pencaharian dan produksi pangan kita. Jika kita melawan perubahan iklim dan beroperasi secara berkelanjutan, kita dapat memastikan cukup makanan untuk kita dan generasi mendatang. Ini adalah langkah lain dalam memerangi krisis pangan</p>
                                        <p>2. Mempromosikan keragaman pangan
                                        Berfokus pada satu tanaman pangan atau bahan pokok dapat menyebabkan hasil yang buruk dalam mengurangi krisis pangan. Oleh karena itu, pelatihan tentang pentingnya pola makan yang beragam dan sehat untuk perbaikan gizi perlu dilakukan dalam rangka meningkatkan ketahanan pangan.</p>
                                        <p>3. Tingkatkan produksi
                                        Tidak ada cara mudah untuk meningkatkan produksi pangan dalam negeri dalam jangka pendek, tetapi kita harus melakukan langkah-langkah untuk meningkatkan produksi pangan dalam negeri dalam jangka panjang.</p>
                                        <p>4. Kurangi Pemborosan
                                        Diperkirakan 20-30% dari semua makanan terbuang dari pertanian .Sayuran bisa rusak jika truk tidak datang cukup cepat, daging bisa rusak jika kulkas tidak cukup dingin, dan prasmanan juga bisa sangat boros. Oleh karena itu, kampanye pendidikan publik yang kuat untuk mengurangi limbah di rumah dan restoran diperlukan, dan mungkin larangan sementara makan prasmanan sampai situasi membaik</p>
                                        <p>5. Memberi Penghargaan
                                        Buat dampak negatif (atau biaya) produksi (misalnya hilangnya lahan) transparan sehingga semua biaya diperhitungkan, dan bukan untuk produksi secara keseluruhan, tetapi untuk penyerapan karbon tanah, meminimalkan input eksternal, mengurangi penggunaan energi menjadi sangat membantu.</p>
                                        <p>6. Sumber protein masa depan
                                        Ide pengembangbiakan serangga dan ubur-ubur hanyalah beberapa dari proposal inovatif yang dibuat untuk mendiversifikasi sumber protein di luar daging, kedelai, dll. Hal ini menyebabkan tingkat protein hewani yang lebih rendah di belahan bumi utara. belahan bumi bagian selatan.</p>

